using System;
using System.Collections.Generic;
using System.Text;

namespace Math_Operations
{
    class Math_Operations
    {
        public int Add(int a,int b)
        {
            return a + b;
        }
        public double Add(double a,double b,double c)
        {
            return a + b + c;
        }
        public decimal Add(decimal a, decimal b, decimal c)
        {
            return a + b + c;
        }
    }
}
